/**
 * @author gl
 */
@Rule(value = "查询企业电子印章",
        group = RuleGroup.腾讯电子签,
        test = true,
        enable = true,
        returnType = ClassType.对象,
        testShowEnum = TestShowEnum.JSON,
        order = 2,
//        iconUrl = "rule-dysmsduanxinfuwu",
        explain = "查询企业印章的列表，需要操作者具有查询印章权限 客户指定需要获取的印章数量和偏移量，数量最多100，超过100按100处理；入参InfoType控制印章是否携带授权人信息，为1则携带，为0则返回的授权人信息为空数组。接口调用成功返回印章的信息列表还有企业印章的总数。"
)
@AllArgsConstructor
public class QueryingEnterpriseElectronicSealsServiceImpl implements BaseCustomFunctionInterface<QueryingEnterpriseElectronicSealsDto> {

    @Override
    public Object execute(QueryingEnterpriseElectronicSealsDto dto, Map<String, Object> params) {
        EssClient client = TenantUtil.getClient(dto.getOptions());
        try {
            // 实例化一个请求对象,每个接口都会对应一个request对象
            DescribeOrganizationSealsRequest req = BeanCopyUtil.copy(dto, DescribeOrganizationSealsRequest.class);
            req.setOperator(BeanCopyUtil.copy(dto.getOperator(), UserInfo.class));
            // 返回的resp是一个DescribeOrganizationSealsResponse的实例，与请求对象对应
            DescribeOrganizationSealsResponse resp = client.DescribeOrganizationSeals(req);
            return BeanCopyUtil.copy(resp, HashMap.class);
        } catch (TencentCloudSDKException e) {
            LOG.error(e);
            throw new RuleException(RuleExceptionEnum.三方平台报错, SpringContextUtil.msg("三方平台执行异常具体信息错误码", e.getMessage(), e.getRequestId(), e.getErrorCode()));
        }

    }
}
